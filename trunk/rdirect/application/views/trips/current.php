(function($){
  // this simple plugin clears a text field on focus, and if it is blank on blur, repopulates
  // the field with the default value
  $.fn.fieldClear = function(options){
    var _ref = this;
    var settings = {'default': "Enter an e-mail address here..."};

    if(options)
      $.extend(settings, options);

    $(this).data('default', settings['default']);

    if(_ref.val() == "")
      _ref.val(settings['default']);

    this.focus(function(){
      if(_ref.val() == settings['default']){
        _ref.val("");
        $(this).addClass('focused');
      }
    });

    this.blur(function(){
      if(_ref.val() == ""){
        $(this).removeClass('focused');
        _ref.val(settings['default']);
      }
    });
  };

  $.fn.pause = function(duration) {
      $(this).animate({ dummy: 1 }, duration);
      return this;
  };
  

  // this plugin handles all the legwork necessary for asynchronously submitting friends' emails, and
  // then waiting for a JSON response
  $.fn.submitItinerary = function(){
    var _ref = this;
    var errorItemCounter = 0;
    var inputCounter = 0;

    var appendInputItem = function(){
      _ref.children('.additional_guests').append('<li><input type="text" name="emails[]"/><span class="error"></span></li>');
      _ref.find('.additional_guests input:text:last').fieldClear();
    };

    var submitJsonForm = function(){
      errorItemCounter = 0;
      inputCounter = 0;

      // do an initial pass to see whether there are any errors. if so, mark them as such.
      _ref.find('.additional_guests input:text').each(function(){
        if(!Airbnb.StringValidator.validate('email', $(this).val())){
          // if the user didn't fill out the form field, just remove it from the DOM
          if($(this).val() == $(this).data('default') || $.trim($(this).val()) == ""){
            $(this).parent().remove();
          }
          // otherwise, mark the input field as an error and tell them that they need to check
          // the address
          else {
          $(this).parent().find('span.error').html('Invalid e-mail');
            errorItemCounter++;
            $(this).addClass('error');
          }
        }
        // if the field validated cleanly, then clean up any previous error messages
        else {
          $(this).removeClass('error');
          $(this).parent().find('span.error').html('');
          inputCounter++;
        }
      });

      // handles the case where there was only one form field that was blank
      if(errorItemCounter == 0 && inputCounter == 0){
        appendInputItem();
        _ref.find('.additional_guests input:text:last').focus();
      }

      // if there are no errors, let's go ahead and process them
      if(errorItemCounter == 0 && inputCounter > 0){
        // serialize the form data
        var serializedFormData = _ref.serialize();

        // iterate over the input fields, removing error messages and
        // replacing the input element with status text and a spinner
        _ref.find('.additional_guests input:text').each(function(){
          $(this).parent().find('span.error').html('');
          $(this).replaceWith('<span class="status">Sending itinerary to ' + $(this).val() + '... <span class="progress"><img alt="Spinner" height="10" src="http://s3.muscache.com/1300304855/images/spinner.gif" /></span></span>');
        });

        // we've replaced all the input elements with status texts, so
        // create a new input element
        appendInputItem();

        // also, bring focus to it so users don't have to use the mouse again
        _ref.find('.additional_guests input:text:last').focus();

        // finally, toss the form data over to the backend
        $.getJSON('/reservation/add_guests', serializedFormData, function(data, textStatus, xhr){
          // handle success and failed cases uniquely. not quite yet...
          data["success"].each(function(v){});
          data["failure"].each(function(v){});

          // update the spinner to 'done', and also fade everything out after a period of time
          _ref.find('span.progress').html('done.').each(function(){
            $(this).parent().pause(1200).fadeOut(function(){
              $(this).parent().remove();        
            });       
          });
        });
      }
    };

    appendInputItem();
    
    // intercept the form submit
    this.submit(function(e){
      submitJsonForm();
      return false;
    });

    this.find('.addanother').click(function(){ 
      appendInputItem();
      return false;
    });

    this.find('.sendemails').click(function(){
      submitJsonForm();
      return false;
    });

  };
})(jQuery);

